namespace Ellimac\Model\Client;


class ClientModel
{
    private $name;
    private $address;
    private $zipCode;
    private $city;

    /**
     * ClientModel constructor.
     * @param $name
     * @param $address
     * @param $code
     * @param $city
     */
    public function __construct($name, $address, $code, $city)
    {
        $this->name = $name;
        $this->address = $address;
        $this->zipCode = $code;
        $this->city = $city;
    }

    /**
     * @return mixed
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return mixed
     */
    public function getAddress()
    {
        return $this->address;
    }

    /**
     * @param mixed $address
     */
    public function setAddress($address)
    {
        $this->address = $address;
    }

    /**
     * @return mixed
     */
    public function getZipCode()
    {
        return $this->zipCode;
    }

    /**
     * @param mixed $zipCode
     */
    public function setZipCode($zipCode)
    {
        $this->zipCode = $zipCode;
    }

    /**
     * @return mixed
     */
    public function getCity()
    {
        return $this->city;
    }

    /**
     * @param mixed $city
     */
    public function setCity($city)
    {
        $this->city = $city;
    }



    function __toString()
    {
       return 'foofoo';
    }

    //TODO: Heute Nachmittag, Model f√ºr Client Klasse schreiben
}